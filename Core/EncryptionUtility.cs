                                 "1", "2", "3", "4", "5", "6", "7", "8", "9", "0"
                            };

            StringBuilder sb = new StringBuilder();
            Random random = new Random();

            for (int i = 0; i < size; i++)
            {
                sb.Append(array[random.Next(0, array.Length)]);
            }

            return sb.ToString();
        }
